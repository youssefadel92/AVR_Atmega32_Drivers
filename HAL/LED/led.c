/******************************************************************************
* \Syntax          : void led_init(void)
* \Description     : this service for initialzing leds
*
* \Sync\Async      : Synchronous
* \Reentrancy      : Non Reentrant
* \Parameters (in) : None
* \Return value:   : None
*******************************************************************************/
void led_init(void)
{
	dio_vidConfigChannel(LED0_PORT, LED0_CHANNEL_NUM, OUTPUT);
	dio_vidConfigChannel(LED1_PORT, LED1_CHANNEL_NUM, OUTPUT);
}

/******************************************************************************
* \Syntax          : void led_on(LED_ID id)
* \Description     : this service for turning on led
*
* \Sync\Async      : Synchronous
* \Reentrancy      : Non Reentrant
* \Parameters (in) : id 	   -> LED ID
* \Return value:   : None
*******************************************************************************/
void led_on(LED_ID id)
{
	switch(id)
	{
		case LED0:
			dio_vidWriteChannel(LED0_PORT, LED0_CHANNEL_NUM, STD_HIGH);
			break;
		case LED1:
			dio_vidWriteChannel(LED1_PORT, LED1_CHANNEL_NUM, STD_HIGH);
			break;

	}
}

/******************************************************************************
* \Syntax          : void led_off(LED_ID id)
* \Description     : this service for turning off led
*
* \Sync\Async      : Synchronous
* \Reentrancy      : Non Reentrant
* \Parameters (in) : id 	   -> LED ID
* \Return value:   : None
*******************************************************************************/
void led_off(LED_ID id)
{
	switch(id)
	{
		case LED0:
			dio_vidWriteChannel(LED0_PORT, LED0_CHANNEL_NUM, STD_LOW);
			break;
		case LED1:
			dio_vidWriteChannel(LED1_PORT, LED1_CHANNEL_NUM, STD_LOW);
			break;
	}
}

/******************************************************************************
* \Syntax          : void led_toggle(LED_ID id)
* \Description     : this service for toggiling led
*
* \Sync\Async      : Synchronous
* \Reentrancy      : Non Reentrant
* \Parameters (in) : id 	   -> LED ID
* \Return value:   : None
*******************************************************************************/
void led_toggle(LED_ID id){
	switch(id)
	{
		case LED0:
			dio_vidFlipChannel(LED0_PORT, LED0_CHANNEL_NUM);
			break;
		case LED1:
			dio_vidFlipChannel(LED1_PORT, LED1_CHANNEL_NUM);
			break;
	}
}

